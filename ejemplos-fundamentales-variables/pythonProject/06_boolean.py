"""
Tener en cuenta que en python las variables booleanas comienzan la primera letra
en mayúscula, True, False
"""

is_single = True
print(is_single)

"""
El operador de negación !, en este caso se puede usar en python con la palabra clave
not. Con not podemos negar las operaciones
"""

print(not False)
print(not True)

print(not is_single)