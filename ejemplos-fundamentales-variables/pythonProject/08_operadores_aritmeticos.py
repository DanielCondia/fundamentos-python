"""
Los operadores aritméticos son los mismos, pero tiene algunas características especiales cuando los combinamos con
strings
"""

print(10 + 10)
print(10 - 5)
print(10 * 2)
print(10 / 2)

# combinado con string se repite tantas veces como lo indiquemos
print('Hola ' * 3)

# Obtenemos solo la parte entera de una division
print(10 // 3)

# Exponente o número elevado
print(2 ** 3)